package association.example.app;

public class Mechanic {
	void name() {
		System.out.println("invoking name from Mechanic");
		System.out.println("mechanic name is xyz");
	}
	void exp() {
		System.out.println("invoking exp from Mechanic");
		System.out.println("experience of mechanic is 10 years");
	}

}
